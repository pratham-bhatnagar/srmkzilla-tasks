console.log("\n'For' Loop example\n")
// For loop
for(let i=1 ; i<11 ; i++){
    console.log('2 x ',i,' = ',2*i);
} 

console.log("\n'While' Loop example\n")
// While loop
let x=0;
while(x<10){
    console.log(x);
    x++
}

console.log("\n'Do while' loop Example\n")
// Do while loop
let y=10;
do{
    console.log(y);
    y+=3
} while(y<20);